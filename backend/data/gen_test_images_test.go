func TestGenerateAllTestImages(t *testing.T) {
	if os.Getenv("GEN_IMAGES") == "" {
		t.Skip("跳过图片生成 (设置 GEN_IMAGES=1 启用)")
	}

	outputDir := `d:\项目\开源项目\go-stock\test`
	if err := os.MkdirAll(outputDir, 0755); err != nil {
		t.Fatalf("创建目录失败: %v", err)
	}

	for i, report := range testReports {
		t.Logf("[%d/10] 生成: %s", i+1, report.filename)

		result, err := markdownToImage(report.md)
		if err != nil {
			t.Errorf("[%d/10] ❌ %s 失败: %v", i+1, report.filename, err)
			continue
		}

		decoded, err := base64.StdEncoding.DecodeString(result[len("base64://"):])
		if err != nil {
			t.Errorf("[%d/10] ❌ %s base64解码失败: %v", i+1, report.filename, err)
			continue
		}

		filename := outputDir + `\` + report.filename + ".png"
		if err := os.WriteFile(filename, decoded, 0644); err != nil {
			t.Errorf("[%d/10] ❌ %s 写入失败: %v", i+1, report.filename, err)
			continue
		}

		t.Logf("[%d/10] ✅ %s (%d bytes)", i+1, report.filename, len(decoded))
	}
}
